<span class="span1">Speaking Truths with Film</span><br> <!--Book id = "1022"-->
<span class="span2">by Bill Nichols</span><br><br>
Pioneering film scholar Bill Nichols investigates the ways documentaries strive for accuracy and truthfulness and simultaneously fabricate a form that shapes reality. Such films may rely on reenactment to re-create the past, storytelling to provide satisfying narratives, and rhetorical figures such as metaphor or devices such as irony to make a point. Documentaries are truly a fiction unlike any other. With clarity and passion, Nichols offers incisive commentaries on the basic questions of documentary’s distinct relationship to the reality it represents, as well as close readings of provocative documentaries from this form's earliest days to its most recent incarnations. These essays offer a definitive account of what makes documentary film such a vital part of our cultural landscape.
