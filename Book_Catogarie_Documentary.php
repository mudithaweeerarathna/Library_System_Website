<p class="book_details">
<span class="span1">The Geo-Doc</span><br> <!--Book id = "1021"-->
<span class="span2">by Terry Mark</span><br><br>
This book introduces a new form of documentary film:<br>
the Geo-Doc, designed to maximize the influential power of the documentary film as an agent of social change. By combining the proven methods and approaches as evidenced through historical, theoretical, digital, and ecocritical investigations with the unique affordances of Geographic Information System technology, a dynamic new documentary form emerges, one tested in the field with the United Nations. This book begins with an overview of the history of the documentary film with attention given to how it evolved as an instrument of social change. It examines theories surrounding mobilizing the documentary film as a communication tool between filmmakers and policymakers. Ecocinema and its semiotic storytelling techniques are also explored for their unique approaches in audience engagement. The proven methods identified throughout the book are combined with the spatial and temporal affordances provided by GIS technology to create the Geo-Doc, a new tool for the activist documentarian.
</p>
